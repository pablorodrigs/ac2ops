package ac2_project.example.ac2_ca.dto;

public class UserDTO {
    private String name;
    private String email;

    // Getters e Setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}

	   
	    
	  
